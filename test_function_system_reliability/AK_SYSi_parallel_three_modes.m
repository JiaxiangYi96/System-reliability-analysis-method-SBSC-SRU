function obj=AK_SYSi_parallel_three_modes(x)
x1=x(:,1);
x2=x(:,2);

obj(:,1)=(4-2.1*x1.^2+x1.^4/3).*x1.^2+x1.*x2+(-4+4*x2.^2).*x2.^2+0.8;
obj(:,2)=100*(4-2.1*x1.^2+x1.^4/3).*x1.^2+100*x1.*x2+100*(-4+4*x2.^2).*x2.^2+60;
obj(:,3)=500*(4-2.1*x1.^2+x1.^4/3).*x1.^2+500*x1.*x2+500*(-4+4*x2.^2).*x2.^2+60;
end